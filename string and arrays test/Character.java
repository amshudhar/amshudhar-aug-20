package exam2;

public class Character {
	
	public static void main(String[] args) {
		
		 String s = "Hello, Java!";
	    
	        char[] c = s.toCharArray();

	        System.out.print("Character array : ");
	        for (char ch : c) {
	            System.out.print(ch + " ");
	        }
		
	}

}
